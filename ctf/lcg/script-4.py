# ref: http://anh.cs.luc.edu/331/code/xgcd.py
def xgcd(a, b):
    x0, x1, y0, y1 = 1, 0, 0, 1
    while b:
        q, r = a // b, a % b
        x1, x0 = x0 - q * x1, x1
        y1, y0 = y0 - q * y1, y1
        a, b = b, r
    return a, x0, y0


x = list(map(int, input().split()))
y = []
for i in range(6):
    y.append(x[i + 1] - x[i])
z1 = y[0] * y[3] - y[1] * y[2]
z2 = y[1] * y[4] - y[2] * y[3]
z3 = y[2] * y[5] - y[3] * y[4]
m1, _, _ = xgcd(z1, z2)
m, _, _ = xgcd(m1, z3)

_, inv, _ = xgcd(x[1] - x[0], m)
a = y[1] * inv % m
b = (x[1] - a * x[0]) % m
x_next = (a * x[6] + b) % m

print("x[7] = ", x_next)
